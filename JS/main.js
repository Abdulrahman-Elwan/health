  // التبديل بين الاهداف

  function changeClass() {
    var myDiv2 = document.getElementById("myDiv2");
    var myDiv = document.getElementById("myDiv");
    // التحقق مما إذا كان الكلاس الحالي هو "initialClass"
    if (myDiv2.classList.contains("u-custom-color-2")) {
      // إذا كان كذلك، قم بإزالته وأضف "changedClass"
      myDiv.classList.remove("u-white");
      myDiv.classList.add("u-custom-color-2");
      myDiv2.classList.remove("u-custom-color-2");
      myDiv2.classList.add("u-white");
    }
  }
  
